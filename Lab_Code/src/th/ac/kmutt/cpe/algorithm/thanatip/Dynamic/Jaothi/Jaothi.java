package th.ac.kmutt.cpe.algorithm.thanatip.Dynamic.Jaothi;

public class Jaothi {

    private int time;
    private int income;

    public Jaothi(int time, int income) {
        this.time = time;
        this.income = income;
    }

    public int getTime() {
        return this.time;
    }

    public int getIncome() {
        return this.income;
    }


}
