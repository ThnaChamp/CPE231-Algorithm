package th.ac.kmutt.cpe.algorithm.thanatip.ForEachLab.EgyptianFraction;

public class Egyptian {

    private int nume;
    private int deno;

    public Egyptian(int nume, int deno) {
        this.nume = nume;
        this.deno = deno;
    }

    public int getNume() {
        return this.nume;
    }

    public int getDeno() {
        return this.deno;
    }

}
